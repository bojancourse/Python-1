# Внесување на броевите m и n од корисникот
m = int(input("Внеси го бројот m: "))
n = int(input("Внеси го бројот n: "))

# Иницијализација на бројачот за броеви со сите парни цифри
count = 0

# Листа за чување на броевите кои ги исполнуваат условите
even_digit_numbers = []

# Итерација низ броевите од m до n
for number in range(m, n + 1):
    # Претпоставка дека сите цифри во бројот се парни
    all_even = True
    
    # Проверка на секоја цифра во бројот
    for digit in str(number):
        if int(digit) % 2 != 0:
            all_even = False
            break

    # Ако сите цифри се парни, го зголемуваме бројачот и го додаваме бројот во листата
    if all_even:
        count += 1
        even_digit_numbers.append(number)

# Печатење на резултатот
print(f"Бројот на броеви од {m} до {n} каде сите цифри се парни е: {count}")
print(f"Броевите се: {even_digit_numbers}")
