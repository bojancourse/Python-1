# Внесување на броевите a, b и c од корисникот
a = int(input("Внеси го бројот a: "))
b = int(input("Внеси го бројот b: "))
c = int(input("Внеси го бројот c: "))

# Иницијализација на бројачот
count = 0

# Пресметка колку броеви помеѓу a и b се деливи со c
for number in range(a, b + 1):
    if number % c == 0:
        count += 1

# Печатење на резултатот
print(f"Бројот на броеви помеѓу {a} и {b} кои се деливи со {c} е: {count}")
