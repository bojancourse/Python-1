# Внесување на бројот од корисникот
number = input("Внеси број: ")

# Иницијализација на празен стринг за превртената верзија
reversed_number = ""

# Итерација преку секој карактер во бројот, започнувајќи од крајот
for i in range(len(number) - 1, -1, -1):
    reversed_number += number[i]

# Печатење на превртената верзија
print(f"Превртената верзија на бројот {number} е: {reversed_number}")
