# Внесување на бројот n од корисникот
n = int(input("Внеси број n: "))

# Принтање на сите прости броеви од 1 до n
print(f"Простите броеви од 1 до {n} се:")

# Итерација низ сите броеви од 1 до n
for num in range(1, n + 1):
    # Ако бројот е помал од 2, тој не може да биде прост
    if num < 2:
        continue

    # Претпоставуваме дека бројот е прост
    is_prime = True

    # Проверка дали бројот има делители освен 1 и самиот број
    for i in range(2, int(num ** 0.5) + 1):
        # Ако се најде делител, бројот не е прост
        if num % i == 0:
            is_prime = False
            break

    # Ако бројот е прост, го печатиме
    if is_prime:
        print(num)

